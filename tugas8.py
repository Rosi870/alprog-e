# ARITMATIKA

x = 24
y = 57

# penjumlahan
hasil = x + y
print("nilai dari x + y =", hasil)

# pengurangan
hasil = x - y
print("nilai dari x - y =", hasil)

# pembagian
hasil = x / y
print("nilai dari x / y =", hasil)

# perkalian
hasil = x * y
print("nilai dari x * y =", hasil)

# eksponen (perpangkatan)
hasil = x ** y
print("hasil dari x ** y =", hasil)

# modulus (sisa bagi)
hasil = x % y
print("hasil dari x % y =", hasil)

# floor division
hasil = x // y
print("hasil dari x //  y =", hasil)